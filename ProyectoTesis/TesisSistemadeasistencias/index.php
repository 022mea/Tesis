                La Unidad Educativa ¨Rosa Olga Villacres Lozano¨ es una institución de educación fiscal mixta orientada a la formación académica de niños, niñas y adolescentes a través del desarrollo de habilidades y destrezas de sus estudiantes, con el fin de impartir sus conocimientos en cada etapa de su formación estudiantil.
