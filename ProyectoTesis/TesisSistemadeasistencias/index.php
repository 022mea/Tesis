                <h2 class="">Historia</h2>
                <p>
                El plantel funciona desde 1920, con el carácter de particular y como directora la profesora Doña Carmen Sánchez Guerrero hasta 1945, luego fue nombrado como profesor fiscal el señor Pedro Lavayen Bodija.
Siendo ministro de Educación el Arquitecto Vera Arrata, crea la Unidad Educativa Básica ¨Rosa Olga Villacres Lozano¨. En el año 2014 asume la Subdirección la Prof. Miriam Quinto Herrera, en este mismo año se crea los 3 años de Bachillerato General Unificado, en el mes de agosto el presupuesto pasa al Distrito Daule 09D19.
En el mes de octubre ingresan a la Institución los Docentes Lcdo. Armando Lamilla Espinoza y la Lcda. Martha Piguave. En el 2016 la Unidad Educativa Rosa Olga Villacrés Lozano cuenta con 39 Docentes. El 27 de marzo se incorpora la primera promoción de Bachilleres en Ciencias, en abril de este mismo año se jubilan la Prof. Nelly Barzola Martillo y la Lcda. Elsa Rodríguez Rubio.
En el periodo lectivo 2017-2018 se trabaja con la jornada espejo es decir que tanto en la jornada matutina como vespertina se ofrece la misma oferta educativa. Desde el 22 de agosto de 2017 la institución cuenta con la “Instalación de Fibra Óptica”
En el 2019 la Prof. Miriam Quinto Herrera, renuncio a el cargo de subdirectora por asuntos personales, asumiendo este cargo la Lcda. Rita Soriano Olvera en la jornada matutina y Lic. Martha Pita Martínez en la jornada vespertina.
En el mes de marzo de 2020 fallece el director de la Institución Prof. Janel Pita Mantilla asumiendo el cargo en el mes de abril el Primer vocal del Consejo Ejecutivo Lcdo. Armando Lamilla Espinoza, en ese entonces la institución contaba con 42 Docentes, 1 Psicopedagoga y 1 Psicóloga Educativa.
A pesar de la difícil situación que existía en el país por la pandemia del COVID 19 se gestionó recursos físicos dando como resultados la donación de puerta de vidrio, 2 perchas metálicas, escritorio armable y escritorio con cajones.
Con la colaboración económica de los compañeros docentes se adquirió un aire acondicionado de segunda mano para la dirección.
Con la ayuda de los padres de familia se logró adquirir una bomba de agua  
Por medio de la junta parroquial se dio el soporte de bioseguridad para los diferentes eventos en la institución: fumigaciones, corte de maleza, capsula de desinfección. 
En marzo del 2021 fallece el Lic. Armando Lamilla Espinoza asumiendo el cargo en el mes de abril la Lic. Ruth Duque Torres como primer vocal del consejo ejecutivo dando así   la continuidad a las gestiones que se venían realizando en beneficio de la comunidad educativa.
Actualmente la unidad educativa cuenta con una planta docente de   43 docentes, 1 Psicopedagoga y 1 Psicóloga Educativa, personal administrativo y de servicio.


